    }

    /**
     * @throws TagNotFoundException
     */
    public function deleteTag(int $id): void
    {
        $tag = $this->find($id);

        if (!$tag) {
            throw new TagNotFoundException('Le tag n\'existe pas.');
        }

        $this->getEntityManager()->remove($tag);
        $this->getEntityManager()->flush();
    }

